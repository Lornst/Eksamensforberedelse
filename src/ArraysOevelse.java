import java.util.Arrays;

public class ArraysOevelse {
    public static void main(String[] args) {
        int arraySize = 2;
        String[][][][][] array = new String[arraySize][arraySize][arraySize][arraySize][arraySize];

        for(int e = 0; e < array.length; e++){
            for (int d = 0; d < array.length; d++) {
                for (int c = 0; c < array.length; c++) {
                    for (int b = 0; b < array.length; b++) {
                        for (int a = 0; a < array.length; a++) {
                            array[a][b][c][d][e] = ("A" + (1 + a)) + "|" + ("B" + (1 + b)) + "|" + ("C" + (1 + c)) + "|" + ("D" + (1 + d)) + "|" + ("E" + (1 + e));
                        }
                    }
                }
            }
        }

        for(String[][][][] a : array){
            for(String[][][] b : a){
                for(String[][] c : b){
                    for(String[] e : c){
                        System.out.print("| " + Arrays.toString(e) + " |");
                    }
                    System.out.println();
                }
                System.out.println("----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------");
            }
            System.out.println("----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------");
        }
    }
}
